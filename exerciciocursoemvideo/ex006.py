print('Crie um algoritmo que leia um número e mostre o seu dobro, triplo e raiz quadrada')
n = int(input('Digite um número'))
n2 = n * 2  # dobro
n3 = n * 3  # triplo
r2 = n ** (1 / 2)  # raiz quadrada
print(f'Seu número é {n}, seu dobro é {n2}, o seu triplo vale {n3} e sua raiz quadrada vale {r2}')
